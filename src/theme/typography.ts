const generateTypoClassName = () =>
  typographyList.reduce((acc, item) => {
    // @ts-ignore
    const name = item.variantName.toLowerCase().replaceAll(' ', '-');

    item.childStyleList.forEach((child) => {
      const value = { ...child };
      // @ts-ignore
      delete value.name;

      // @ts-ignore
      acc[`.text-${name}-${child.name}`] = value;
    });

    return acc;
  }, {});

